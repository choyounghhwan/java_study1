package ch02.sec02;

public class LongExample {

	public static void main(String[] args) {
		
        long var1 = 10;
        long var2 = 20L;
        //long var3 = 1000000000000; // long 형에는 무조건 'L'을 붙여야 한다.
        long var4 = 1000000000000L;
        
       
        System.out.println(var1);
        System.out.println(var2); // = System.out.print(var2 + "\n");
        System.out.println(var4);
       
	}

}
